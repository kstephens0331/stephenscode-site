export function generateColorVars(palette: ColorPalette): React.CSSProperties {
  return {
    '--demo-primary': palette.primary,
    '--demo-primary-dark': palette.primaryDark,
    '--demo-primary-light': palette.primaryLight,
    '--demo-secondary': palette.secondary,
    '--demo-accent': palette.accent,
    '--demo-bg': palette.background,
    '--demo-bg-alt': palette.backgroundAlt,
    '--demo-text': palette.text,
    '--demo-text-light': palette.textLight,
    '--demo-border': palette.border,
    '--demo-success': palette.success,
    '--demo-error': palette.error,
    '--demo-warning': palette.warning
  } as React.CSSProperties
}
